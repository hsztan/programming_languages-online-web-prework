def reformat_languages(languages)
  new_hash = {}
  languages.each do |style, language|
    new_hash.merge!(language)
    language.each do |key, value|
      new_hash[key].merge!({:style => [style]})
    end
  end
  new_hash
end

puts reformat_languages(languages)
